package com.sd.farmework.pojo;

import com.sd.farmework.common.BaseInfo;

public class HqNoticeInfo extends BaseInfo{
	
	private String noticeId;
	private String noticeTitle;
	private String noticeContent;
	private String departId;
	private String unitId;
	private String departName;
	private String unitName;
	public String getNoticeId() {
		return noticeId;
	}
	public void setNoticeId(String noticeId) {
		this.noticeId = noticeId;
	}
	public String getNoticeTitle() {
		return noticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}
	public String getNoticeContent() {
		return noticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}
	public String getDepartId() {
		return departId;
	}
	public void setDepartId(String departId) {
		this.departId = departId;
	}
	public String getUnitId() {
		return unitId;
	}
	public void setUnitId(String unitId) {
		this.unitId = unitId;
	}
	public String getDepartName() {
		return departName;
	}
	public void setDepartName(String departName) {
		this.departName = departName;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	@Override
	public String toString() {
		return "HqNoticeInfo [noticeId=" + noticeId + ", noticeTitle="
				+ noticeTitle + ", noticeContent=" + noticeContent
				+ ", departId=" + departId + ", unitId=" + unitId
				+ ", departName=" + departName + ", unitName=" + unitName + "]";
	}
	
}
