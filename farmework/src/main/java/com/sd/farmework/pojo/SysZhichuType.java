package com.sd.farmework.pojo;

import com.sd.farmework.common.BaseInfo;
/**
 * 支出/请假类别表
 * @author Administrator
 *
 */
public class SysZhichuType extends BaseInfo{
	private Integer  mainId;		//主键流水号
	private String   zcId;			//支出类型编号
	private String   zcTypeName;	//支出类型名称
	private String   status;		//状态
	
	
	
	public Integer getMainId() {
		return mainId;
	}
	public void setMainId(Integer mainId) {
		this.mainId = mainId;
	}
	public String getZcId() {
		return zcId;
	}
	public void setZcId(String zcId) {
		this.zcId = zcId;
	}
	public String getZcTypeName() {
		return zcTypeName;
	}
	public void setZcTypeName(String zcTypeName) {
		this.zcTypeName = zcTypeName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	@Override
	public String toString() {
		return "SysZhichuType [mainId=" + mainId + ", zcId=" + zcId
				+ ", zcTypeName=" + zcTypeName + ", status=" + status + "]";
	}
	
	
}
